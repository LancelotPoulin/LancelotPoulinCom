       <div class="etape"> <h1 id="ETAPE1"> ETAPE 1 : Etablir son projet de mobilité </h1>
		
		<p> Vous envisagez de partir faire des études à l'étranger. </p>
		
		<p> 1) Faire des recherches sur le tableau des possibilités d'échange en ligne à partir du <span class="important">1er octobre</span> afin de cibler les universités avec lesquelles un accord a été défini dans la spécialité souhaitée. </p>
		
		<p> 2) Contacter le correspondant des Relations Internationales de la PO ou du département concerné pour faire valider votre projet. Attention, certains correspondants RI de PO ou de département ont une procédure spécifique pour le choix des destinations (via une page sous moodle ou autre). Vous devez vous y conformer et respecter également la procédure générale de la DRI. </p>

		<p> <span class="important">Quelques remarques :</span> </p>
		
		<ol> 
			<li> Le tableau indique le nombre de places disponibles par destination et par discipline. </li>
		
			<li> L'intitulé des codes disciplines se trouve sur une page à part dans le fichier. </li>
		
			<li> Vous devez respecter les spécialités INSA apparaissant dans le fichier, faute de quoi votre demande d'affectation ne pourra pas être satisfaite. </li>
		
			<li> Le niveau L(licence) correspond à un départ en 3A, le niveau M (master) à un départ en années 4 et 5. Certaines universités sont très strictes sur ces niveaux, d'autres beaucoup moins. </li>
		
		    <li> Vous devez faire très attention aux remarques indiquées dans le fichier, sinon votre départ pourra être compromis. </li>
		
			<li> Si vous envisagez un départ en semestre d'études en 4ème année, celui-ci doit être compatible avec les PTP choisis (voir fichier ci-dessous). </li>
		</ol>

		<p class="critique"> <b>ATTENTION</b>, Certaines destinations apparaissent avec des mentions particulières: </p>

		<p> 1) Toutes les destinations du Royaume Uni sont <span class="important">suspendues aux décisions du Brexit</span>. En cas de Brexit, nous ne savons pas à ce jour si les universités britanniques resteront dans le programme erasmus+, ni si elles souhaiteront poursuivre des échanges d'étudiants sous une autre forme. La seule qui se soit manifesté est l'Univ. de Leicester qui souhaite poursuivre les échanges. </p>

		<p> 2) Les accords qui apparaissent avec la mention "en cours de signature" seront renouvelés, mais nous ignorons à quelle date; ceux qui apparaissent avec la mention "en cours de renouvellement" sont en discussion, nous ne pouvons pas garantir à 100% qu'ils le seront. </p>

		<p> <span class="important">Inutile de nous poser des questions à ce sujet, nous n'avons pas plus d'informations à vous donner et il ne sera répondu à aucun mail demandant des informations complémentaires. </span> </p> </div>
		
