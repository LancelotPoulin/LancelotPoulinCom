		<div class="etape"><h1 id="ETAPE6"> ETAPE 6 : Début de la mobilité </h1>

		<p> L'étudiant transmet directement à la DRI une attestation d'installation dans un <span class="important">délai maximum de 3 semaines</span> après le début du semestre de l'université d'accueil. </p>

		<p> L'étudiant transmet directement à son département d'origine à l'INSA, son contrat d'études définitif dans un délai maximum de 3 semaines après le début du semestre de l'université d'accueil. Le correspondant RI accuse réception de ce programme d'études, l'approuve et le transmet à la DRI pour signature. En cas de modification du contrat d'études pendant le séjour, l'étudiant sollicite préalablement l'accord formel du  correspondant RI. </p>

		<p> En cours de semestre, informer rapidement la DRI ou le correspondant RI de tout problème majeur relatif à ses conditions d'accueil et/ou au déroulement de ses études. </p></div>

		<div id="anchorETAPE7">
    				</br>
    			</div>


		<div class="etape"><h1 id="ETAPE7"> ETAPE 7 : Fin de la mobilité </h1>

		<p> L'étudiant fait remplir l'attestation de fin de séjour transmise par Virginie Gary DRI et la lui retourne. </p>

		<p> L'étudiant s'organise pour que sa scolarité (examens compris) dans l'université d'accueil soit terminée dans un délai compatible avec le calendrier de l'INSA. Pour les périodes correspondant au 1er semestre, cette scolarité doit être terminée au plus tard le 1er février sauf contrainte particulière liée à l'Université d'accueil et accord écrit du correspondant RI. Pour les périodes correspondant au 2ème semestre, cette scolarité doit être terminée au plus tard le 1er septembre. </p>
		
		<p> L'étudiant transmet directement à l'INSA une copie du relevé de notes (ou s'organise pour qu'une copie soit transmise par l'université d'accueil), <span class="important">signée par un représentant autorisé</span> de l'université d'accueil. Pour les périodes correspondant au 1er semestre, cette copie doit être transmise au plus tard le 20 juin. Pour les périodes correspondant au 2ème semestre, cette copie doit être transmise au plus tard le 20 juin pour un examen des résultats à la session du jury de juin et au plus tard le 1er septembre pour un examen des résultats à la session du jury de septembre. </p>
		
		<p> Les dispositions précédentes sont extraites du "Règlement pour les séjours d'études à l'étranger", annexé au règlement des études INSA. </p></div>

