		<div class="etape" id="ETAPE2"><h1 id="ETAPE1"> ETAPE 2 : S'inscrire sur moveonline </h1>

		<p> 1) Une fois votre projet de mobilité validé par le correspondant RI de la PO ou du département, s'inscrire sur le logiciel <span class="important">MoveOnLine </span>(accessible durant le mois de novembre) et faire 3 vœux de destinations en vous connectant à l'aide de vos identifiants habituels. </p>

		<p> Vous devez indiquer votre numéro étudiant INE (Identifiant National Etudiant composé de 10 chiffres et une lettre) qui se trouve sur votre carte d'étudiant. Vous pouvez saisir vos 3 vœux jusqu'à la fin novembre (pour faire 3 vœux, il suffit de cliquer sur "plus" à la fin chaque vœu de mobilité). Vous pouvez modifier votre candidature tant que vous ne l'avez pas validée (c'est-à-dire tant qu'elle n'a pas été envoyée). </p>

		<p> 2) Pour chacun des 3 vœux, remplir une fiche projet et un programme d'études "learning agreement" et les donner au correspondant RI du département qui les conserve. </p>

		<p class="critique"> <b>ATTENTION</b>: seuls les projets validés par le correspondant RI seront pris en considération par la commission de sélection. </p></div>

		<div id="anchorETAPE3">
