		<div class="intro"><h1> Généralités </h1>

		<p> Dans le cadre de leur formation, les élèves ingénieurs de l'INSA de Toulouse ont l'obligation de partir à l'étranger pour une<span class="important"> durée minimale de 12 semaines</span>. </p>
		
		<p> Ce séjour peut être effectué sous forme d'un ou plusieurs stages (en entreprise ou en laboratoire de recherche) ou d'un séjour d'études dans une université partenaire (semestre ou préparation d'un double diplôme). Il est également possible de participer à une <span class="important">"summer school"</span>. La page International : summer schools vous propose une liste de "summers schools" organisées par des universités partenaires (à consulter entre décembre et mai pour un départ dans l'été qui suit).. </p>

		<p> La Direction des Relations Internationales aide les étudiants dans la préparation d'un séjour d'études. </p>

		<p> En ce qui concerne les stages à l'étranger, les étudiants doivent s'adresser au responsable des stages de leur département et/ou au secrétariat 4/5. Une page moodle recense néanmoins quelques possibilités de stages en laboratoire dans des universités partenaires (cf. International : stages à l'étranger). Dans tous les cas, il faut suivre la procédure concernant les stages mise en place dans votre département. </p>

		<p> Vous trouverez ci-dessous des explications sur les démarches à accomplir pour préparer un semestre d'études à l'étranger.  Sept étapes sont à respecter (voir ci-dessous). </p>

		<p>L'<a class="Liens" href="#Annexe">Annexe</a> vous informe sur les différentes possibilités de financement (séjour d'études ou stage). </p>

        <p>  <span class="important">La page International : préparez votre double-diplôme </span> (sur moodle) vous donne des informations sur les possibilités de préparer un double-diplôme avec un établissement partenaire. </p>

