		<p> Il est <span class="important">obligatoire</span> de s'inscrire administrativement à l'INSA avant le départ. Il est possible de le faire par correspondance en utilisant le formulaire d'inscription par correspondance de la scolarité (à renvoyer avant le 12 septembre). </p></div>

