		<div class="etape"><h1 id="ETAPE4"> ETAPE 4 : Préparation du Séjour </h1>
		 
		<p> Pour les étudiants autorisés par la commission à candidater auprès d'une université étrangère partenaire : </p>

		<p> Passer à la DRI signer votre affectation. </p>
 		
 		<p> Visiter le site Internet de l'Université choisie pour s'assurer de la date limite d'envoi du dossier de candidature et de la liste des documents à fournir pour constituer le dossier.</p>
		
		<p class="critique"> <b>ATTENTION</b>: le TOEFL est obligatoire pour les USA et pour un nombre croissant de partenaires dans le monde.</p>
		
		<p> Finaliser le learning agreement (télécharger le <span class="important">learning agreement Outgoing</span>, voir fichier ci-dessous) comportant la liste des cours à suivre à l'étranger et la faire signer par le correspondant RI. </p>

		<p> Suivant les universités, le dossier est à remplir en ligne ou bien à transmettre à l'université (pdf ou version papier). Dans le cas du dossier à remplir en ligne, c'est à vous de le faire; dans le cas d'un envoi par mail ou par courrier, c'est la DRI qui s'en charge. </p>

		<p>Apporter le dossier complet non agrafé à la DRI (Virginie Gary) qui en effectuera l'enregistrement, la copie et l'envoi à l'université d'accueil.</p> <p class="critique"><b>ATTENTION</b>: la DRI n'acceptera pas de dossiers à moins de 10 jours de la date limite de réception par l'université d'accueil pour un séjour en Europe  et 15 jours pour un séjour hors Europe. </p>
		
		<p>Si l'inscription se fait en ligne, apporter tout de même à la DRI la copie papier de la demande de candidature. Vous pourrez compléter la  fiche  "Budget" pour vous aider dans vos prévisions budgétaires. </p>
		
		<p>A réception de l'acceptation ou du refus de candidature, prévenir la DRI (Virginie Gary), réciproquement si la DRI reçoit une réponse, l'étudiant sera prévenu immédiatement par mail ou téléphone. </p>
		
		<p class="critique"><b>Important</b>: La DRI envoie courant Mai à tous les étudiants susceptibles de partir en semestres d'études plusieurs emails les informant de la procédure à suivre pour obtenir des aides financières. Voir également l'<a class="Liens" href="#Annexe">annexe</a> 2.</p>

		<p class="critiquecritique"> <b>Important</b>: Merci de prendre note des consignes de sécurité données par le Fonctionnaire Sécurité Défense de l'INSA. Consulter les pages du site Web du Ministère des Affaires étrangères "Conseils aux voyageurs" concernant le pays où se fera la mobilité. Vous devez également vous inscrire sur le site ARIANE du MAE. </p>

		<p> L'obtention du visa ainsi que toutes les démarches concernant le logement et les assurances (médicale, responsabilité civile, logement,...) sont du  <span class="important">ressort de l'étudiant.</span> </p>

		<p class="critique"> <b>ATTENTION</b>: pour les étudiants de nationalité non européenne, il faut IMPÉRATIVEMENT se préoccuper AVANT le départ des formalités permettant le retour en France (par exemple si le titre de séjour expire pendant le séjour à l'étranger). </p>

		<p> L'Université Fédérale Toulouse Midi-Pyrénées propose la <span class="important">Mouv’Box </span>(mouvbox.univ-toulouse.fr), nouveau dispositif d’accompagnement de la mobilité sortante. </p>

		<p> Vous trouverez sur le site web de la Mouv'Box, des fiches pays, des informations pratiques sur les démarches administratives, des témoignages d’étudiants et de professionnels… Il est même possible de prendre un rendez-vous avec l’outil prise de rendez-vous, qui facilite la rencontre avec un conseiller en mobilité qui fera le point avec vous sur votre projet et répondra à toutes vos questions. </p>

		<p> Si pour une raison ou une autre, vous abandonnez votre projet (redoublement, problèmes personnels,...), vous avez <span class="important">l'obligation</span> de prévenir la DRI (Mme Gary) et votre correspondant RI. </p></div>

