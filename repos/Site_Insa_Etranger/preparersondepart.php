		<div class="etape"><h1 id="ETAPE3"> ETAPE 3 : Analyse des candidatures </h1>

		<p> Une commission composée des correspondants RI et de membres de la Direction des Relations Internationales se réunit plusieurs fois en décembre et janvier pour affecter les étudiants sur une <span class="important">UNIQUE</span> destination choisie parmi les 3 vœux déposés sur moveonline, sous réserves bien sûr que ces vœux aient été validés par le correspondant RI. </p>

		<p> Le critère de sélection est <span class="important">principalement le classement de l'année précédente</span>, éventuellement l'implication dans les activités liées à l'accueil des étudiants étrangers sur le campus de l'INSA. </p>

		<p> Pour les étudiants qui n'ont pas eu d'affectation après ces commissions, un <span class="important">second tour </span>est organisé fin janvier avec les places restantes leur permettant de déposer à nouveau 3 voeux. Ce second tour est ouvert également aux élèves de 1ère année, aux étudiants qui n'avaient pas candidaté au premier tour et à ceux qui sont classés au-delà de 80% (sous réserves du soutien de leur correspondant RI). </p></div>

